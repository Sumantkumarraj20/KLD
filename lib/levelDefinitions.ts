/**
 * Complete level map
 */
export const AllLevelDefinitions: DomainLevelMap = {
  language: LanguageLevels,
  mathematics: MathematicsLevels,
  logical: LogicalLevels,
};

/**
 * Helper: Get level definition
 */
export function getLevelDefinition(domain: string, levelNumber: number): LevelDefinition | null {
  const levels = AllLevelDefinitions[domain as keyof DomainLevelMap];
  if (!levels) return null;
  return levels.find((l) => l.levelNumber === levelNumber) || null;
}
