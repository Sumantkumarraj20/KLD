/**
 * Level Definitions - 50 levels per domain
 * Evidence-based cognitive progression using:
 * - Bloom's Taxonomy (knowledge → comprehension → application)
 * - Zone of Proximal Development (scaffolded learning)
 * - Spaced Repetition principles
 */

export interface LevelDefinition {
  levelNumber: number;
  title: string;
  description: string;
  learningObjectives: string[];
  pedagogicalApproach: string;
  difficulty: "easy" | "medium" | "hard";
  estimatedDuration: number; // minutes
  bloomsLevel: "remember" | "understand" | "apply" | "analyze" | "evaluate" | "create";
  contentGuide: string;
}

export interface DomainLevelMap {
  language: LevelDefinition[];
  mathematics: LevelDefinition[];
  logical: LevelDefinition[];
}

